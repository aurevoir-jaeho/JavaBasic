package ch02;

public class Variable1 { // 클래스 이름은 대문자 시작

	public static void main(String[] args) {
		int level; // 선언, 변수 이름은 소문자 시작
		level = 10; // 초기화
		System.out.println(level); //  출력, 단축키(sysout + Ctrl + Space)
	} // end of class
} // end of main
